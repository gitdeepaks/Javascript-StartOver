const x = Math.random() * 100 + 1;
const y = Math.random() * 50 + 1;

//Get the sum
const sum = x + y;
const sumTotal = `${x} + ${y} = ${sum}`
console.log(sumTotal)


//get the difference
const difference = x - y
const getDifference = `${x} - ${y} = ${difference}`
console.log(getDifference)

//get the product
const prod = x * y
const getProd = `${x} x ${y} = ${prod}`
console.log(getProd)

//get the quotient
const quot = x / y
const getQuot = `${x} / ${y} = ${quot}`
console.log(getQuot)

